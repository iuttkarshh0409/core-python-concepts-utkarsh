tech=["Git","Linux","Python"]
tech.append("Docker") #adding Docker at the end of the list
print(tech)
tech.insert(1,"Bash")  #adding Bash at index 1
print(tech)
tech.remove(tech[2]) #removing Linux from the list
print(tech)
tech.pop(3) #popping the last element
print(tech)
tech.sort() #sort the list in ascending order
print(tech)
tech.reverse() #reversing the list elements (in descending order)
print(tech)

